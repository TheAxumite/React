function formatTime(date) {
  return new Intl.DateTimeFormat('en-US', {
      hour: '2-digit', 
      minute: '2-digit', 
      second: '2-digit',
      hour12: true // Use 24-hour format; set to true for 12-hour format
  }).format(date);
}

function formatDate(date) {

  return new Intl.DateTimeFormat(
    'en-US',
    { weekday: 'long' }
  ).format(date);

}

